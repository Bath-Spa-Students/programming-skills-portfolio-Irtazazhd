# Assigning the value 'green' to the variable alien_color
alien_color = 'green'

# If-else chain to test the alien's color
if alien_color == 'green':
    # This block runs because alien_color is 'green'.
    print("You just earned 5 points for shooting the alien!")
else:
    # This block will not execute because the if condition is true.
    print("You just earned 10 points for shooting the alien!")

# Assigning a value other than 'green' to the variable alien_color, let's choose 'red'.
