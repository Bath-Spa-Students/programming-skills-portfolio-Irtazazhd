# Prompting the user to enter the number 5
user_input = input("Please enter the number 5: ")

# Type casting the input into different data types
string_version = str(user_input)
int_version = int(user_input)
float_version = float(user_input)

# Printing the different type-casted versions of the input
print("String version:", string_version)
print("Integer version:", int_version)
print("Float version:", float_version)
